    /**
     * Methode pour charger les champs du formulaire avec les valeurs du Client
     * a modifier
     */
    private void champ_A_Modifier(){
        jTbx_NumIdentification.setText(String.valueOf(leProspect.getIdent()));
        jTbx_RaisonSociale.setText(leProspect.getRaison());
        jCbx_TypeSociete.setSelectedItem(leProspect.getTypeSo());
        jTbx_Domaine.setText(leProspect.getDomaine());
        jTbx_Adresse.setText(leProspect.getAdresse());
        jTbx_NumTel.setText(leProspect.getTel());
        jTbx_ChiffreAffaire.setText(String.valueOf(leProspect.getCa()));
        jTbx_NbEmploye.setText(String.valueOf(leProspect.getNbrEmp()));
        jXDtp_DateProspection.setDate(leProspect.getDateProspetc());
        if(leProspect.getInteretPropspect()){
            jCkbx_InteretTrue.setSelected(true);
        }else{
            jCkbx_InteretFalse.setSelected(true);
        }
        jTxa_Commentaires.setText(leProspect.getComment());
    }    
    
    /**
     * Methode qui controle que tous les champs du formulaire sont valides
     * et recupere la valeur de ses champs
     * @return 
     */
    private boolean validFormulaire(){
        //  On initialise la variable a false
        boolean bValid = false;
        //  On instancie un objet Controle_Regex
        regProspect = new Controle_Regex();
        try {
            //  On verifie que toutes les saisies sont valides 
            if(regProspect.controlRaison(jTbx_RaisonSociale.getText())
                && regProspect.controlDomaine(jTbx_Domaine.getText())
                && regProspect.controlAdresse(jTbx_Adresse.getText())
                && regProspect.controlNumTel( jTbx_NumTel.getText())
                && regProspect.controlChiffrAffaire(jTbx_ChiffreAffaire.getText())
                && regProspect.controlNbEmploye(jTbx_NbEmploye.getText())
                && controlDateContrat() && controlInteret()
                && regProspect.controlCommentaire(jTxa_Commentaires.getText())){ 
                
                //  Si tout est bon
                bValid = true;
                
                //  Si on cree un Prospect
                if(!bModif){
                    //  Affichage du numero d identification du Prospect a creer 
                    //  en appelant la methode de class crea_NumIdProspect()
                    this.jTbx_NumIdentification.setText(String.valueOf(Prospect.crea_NumIdProspect()));
                }              
                //  On recupere la valeur des champs saisies
                numId = Integer.parseInt(jTbx_NumIdentification.getText());
                //  Message pour afficher a l utilisateur le numero d identification cree
                if(!bModif){showMessageDialog(null, "Création du Prospect avec le numéro d'identification : "
                        + numId);}
                raison = jTbx_RaisonSociale.getText(); 
                type = jCbx_TypeSociete.getSelectedItem().toString();
                domaine = jTbx_Domaine.getText();            
                adresse = jTbx_Adresse.getText();            
                tel =  jTbx_NumTel.getText();            
                chiffre = Integer.parseInt(jTbx_ChiffreAffaire.getText());                          
                nbrEmp = Integer.parseInt(jTbx_NbEmploye.getText());            
                commentaire = jTxa_Commentaires.getText();            
                date = jXDtp_DateProspection.getDate();            
                interest = choixInteret();  
                //  On instancie un Prospect avec les valeurs saisies
                try {
                    leProspect = new Prospect(numId, raison, type, domaine, adresse, tel, chiffre, commentaire, nbrEmp, date, interest);
                } catch (Exception ex) {
                    Logger.getLogger(Formulaire_Prospect.class.getName()).log(Level.SEVERE, null, ex);
                }
            }            
        }catch(Exception ex){
            showMessageDialog(null, ex.getMessage());
            bValid = false;
        }        
        return bValid;
    }               
  
