     /**
     * Methode qui controle la validite de la date de contrat
     * @return 
     */
    private boolean controlDateContrat(){
        boolean bDateCont = false;
        Date dateNow = Calendar.getInstance().getTime();
        Date inputDate = jXDtp_DateProspection.getDate();
        if(inputDate.after(dateNow)){
            showMessageDialog(null, "La date de contrat ne peut pas être supérieur à la date du jour");
        }
        else{
            bDateCont = true;
        }
        return bDateCont;
    }  
 
    //--------------------------Les CheckBox------------------------------------
    
    /**
     * Methode qui controle qu'une des deux CheckBox a été coché
     * @return 
     */
    private boolean controlInteret(){
        boolean bInteret;
        if((jCkbx_InteretTrue.isSelected()) || (jCkbx_InteretFalse.isSelected())){
            bInteret = true;
        }
        else{
            bInteret = false;
            showMessageDialog(null, "Veuillez cocher l'intérêt du Prospect");
        }
        return bInteret;
    }         
    
    /**
     * Methode qui retourne si le Prospect et interesse ou pas
     * @return 
     */
    private boolean choixInteret(){
        boolean bChoix = (jCkbx_InteretTrue.isSelected()) ? true : false;
        return bChoix;
    }
    
