    /**
     * Methode associee au bouton valider qui apres verification de la saisie
     * utilisateur creer ou met a jour un Prospect
     * @param evt 
     */
    private void jBtn_ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_ValiderActionPerformed
        if(validFormulaire()){
            //  Si formulaire de modification
            if(bModif){                              
                // On appelle la methode d instance de creation
                leProspect.MiseAJour();
            }
            //  Sinon c est une creation
            else {
                //  On appelle la methode d instance de mise a jour
                leProspect.Creation();
            }            
            //  On instancie une nouvelle fenetre d 'accueil qui sera mise a jour
            JFrame_Accueil ecranAccueil = null;
            try {
                ecranAccueil = new JFrame_Accueil(1);
            } catch (Exception ex) {
                Logger.getLogger(Formulaire_Client.class.getName()).log(Level.SEVERE, null, ex);
            }
            ecranAccueil.setVisible(true);
            this.dispose();    
        }        
        else{
            showMessageDialog(null, "Erreur lors de la validation du formulaire !");
        }
    }//GEN-LAST:event_jBtn_ValiderActionPerformed

