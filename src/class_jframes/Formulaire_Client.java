    /**
     * Methode pour charger les champs du formulaire avec les valeurs du Client
     * a modifier
     */
    private void champ_A_Modifier(){
        jTbx_NumIdentification.setText(String.valueOf(leClient.getIdent()));
        jTbx_RaisonSociale.setText(leClient.getRaison());
        jCbx_TypeSociete.setSelectedItem(leClient.getTypeSo());
        jTbx_Domaine.setText(leClient.getDomaine());
        jTbx_Adresse.setText(leClient.getAdresse());
        jTbx_NumTel.setText(leClient.getTel());
        jTbx_ChiffreAffaire.setText(String.valueOf(leClient.getCa()));
        jTbx_NbEmploye.setText(String.valueOf(leClient.getNbrEmp()));
        jXDtp_DateContrat.setDate(leClient.getDateContrat());
        jTbx_AdrMail.setText(leClient.getAdresseMail());
        jTxa_Commentaires.setText(leClient.getComment());
    }
    
    /**
     * Methode qui controle que tous les champs du formulaire sont valides
     * et recupere la valeur de ses champs
     * @return 
     */
    private boolean validFormulaire(){
        //  On initialise la variable a false
        boolean bValid = false;
        //  On instancie objet Controle_Regex
        regClient = new Controle_Regex();
        try {
            //  On verifie que toutes les saisies sont valides 
            if(regClient.controlRaison(jTbx_RaisonSociale.getText())
                && regClient.controlDomaine(jTbx_Domaine.getText())
                && regClient.controlAdresse(jTbx_Adresse.getText())
                && regClient.controlNumTel( jTbx_NumTel.getText())
                && regClient.controlChiffrAffaire(jTbx_ChiffreAffaire.getText())
                && regClient.controlNbEmploye(jTbx_NbEmploye.getText())
                && controlDateContrat()
                && regClient.controlMail(jTbx_AdrMail.getText())
                && regClient.controlCommentaire(jTxa_Commentaires.getText())
                && erreurCa_Emp()){ 
                
                //  Si tout est bon
                bValid = true;
                //  On recupere la valeur des champs saisies                
                raison = jTbx_RaisonSociale.getText(); 
                type = jCbx_TypeSociete.getSelectedItem().toString();
                domaine = jTbx_Domaine.getText();            
                adresse = jTbx_Adresse.getText();            
                tel =  jTbx_NumTel.getText();            
                chiffre = Integer.parseInt(jTbx_ChiffreAffaire.getText());                          
                nbrEmp = Integer.parseInt(jTbx_NbEmploye.getText());            
                commentaire = jTxa_Commentaires.getText();            
                date = jXDtp_DateContrat.getDate();            
                mail = jTbx_AdrMail.getText();                              
            }            
        }catch(Exception ex){
            showMessageDialog(null, ex.getMessage());
            bValid = false;
        }        
        return bValid;
    }
    
