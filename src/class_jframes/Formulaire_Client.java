     /**
     * Methode qui controle la validite de la date de contrat
     * @return 
     */
    private boolean controlDateContrat(){
        boolean bDateCont = false;
        Date dateNow = Calendar.getInstance().getTime();
        Date inputDate = jXDtp_DateContrat.getDate();
        if(inputDate.after(dateNow)){
            showMessageDialog(null, "La date de contrat ne peut pas être supérieur à la date du jour");
        }
        else{
            bDateCont = true;
        }
        return bDateCont;
    }    
    
    //------------Controle de la coherence CA / nombre employe(s)---------------
    
    /**
     * Methode qui verifie que le chiffre d affaire n est pas 1 000 000 superieur
     * au nombre d employ(s)
     * @return 
     */
    private boolean erreurCa_Emp(){
        boolean bStatus = false;
        int ca = Integer.parseInt(jTbx_ChiffreAffaire.getText());
        int emp = Integer.parseInt(jTbx_NbEmploye.getText());
        if(ca > 1000000*emp){
            showMessageDialog(null, "Problème de cohérence entre le chiffre d'affaire "
                    + "et le nombre d'employé(s)");
        }else{
            bStatus = true;
        }
        return bStatus;
    }
